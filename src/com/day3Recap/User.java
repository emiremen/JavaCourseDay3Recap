package com.day3Recap;

public class User {
    int id;
    String name;
    String surname;
    String mail;
    String password;


    public User(int id, String name, String surname, String mail, String password) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.mail = mail;
        this.password = password;
    }


}
